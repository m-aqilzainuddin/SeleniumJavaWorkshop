package workshopday1;

public class LoopTimeTableNine {
    public static void main(String[] args) {

        System.out.println("Multiplication Table of 9:");

        for (int i = 1; i <= 10; i++) {
            int result = 9 * i;
            System.out.println("9 * " + i + " = " + result);
        }
    }
}